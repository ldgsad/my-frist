void _74HC595_Write(unsigned char Byte)
{
	unsigned char i;
	for(i=0;i<8;i++)
	{
		SER=Byte&(0x80>>i);
		SCK=1;//串行输入下降沿
		SCK=0;
	}
	RCK=1;//并行输入下降沿
	RCK=0;
}


void MatrixLED_ShowRow(unsigned char Column,Data)//行扫描，每次也只送一行的行数据
{
	/*列扫描*/
	_74HC595_Write(Data);
	P0=~(0x80>>Column);
	/*行扫描*/
//	P0=~Data;//低有效
//	_74HC595_Write(0x01<<ROW);//扫描，高有效
	delay(1);
	_74HC595_Write(0x00);
}

